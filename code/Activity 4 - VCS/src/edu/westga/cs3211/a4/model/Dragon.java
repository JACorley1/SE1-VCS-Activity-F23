package edu.westga.cs3211.a4.model;

public class Dragon {

	private String name;
	private int age;
	
	
	
	public Dragon(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
